public class if_else {
    public static void main(String[] args){
        int nota = 30;
        int nota_minina = 65;

        if (nota >= nota_minina){
            System.out.println("Voce passou de ano");
        }else if (nota >= 40){
            System.out.println("Voce ficou de recuperacao");
        }else{
            System.out.println("Voce reprovou");
        }    

        
        
    }
}
